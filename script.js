const close = document.getElementById("close");

let cardInnerNode;
let filterCardData;
let initialCard = 4;

const showCards = () => {
    filterCardData.forEach((el, index) => {
        let card = document.createElement("div");
        card.className = "card"
        let even = index % 2 === 0;
        if (even) {
            cardInnerNode = `
                <img class="card-img" src=${el.image} alt="discussion">
                <div class="bottom-text">
                    <p class="heading">
                        ${el.title}
                        <span class="subHeading">${el.subTitle}</span>
                    </p>
                    <img onClick=showModal("${el.videoTitle}") class="play-icon" src="./assets/images/play.png" alt="icon">
                </div>
            `;
        } else {
            cardInnerNode = `
                <div class="top-text">
                <p class="para">
                ${el.title}
                </p>
                <img class="company-logo" src=${el.logo} alt="icon">
                </div>
                <img class="card-img" src=${el.image} alt="Investment discussion">
           `;

        }
        card.innerHTML = cardInnerNode;
        cardWrapper.appendChild(card)
    });
};

